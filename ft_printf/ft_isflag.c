#include "ft_printf.h"

int		ft_isflag(int c)
{
	if ((char)c == '0' || (char)c == '-' || (char)c == '+' || (char)c == ' ' ||
			(char)c == '#' || (char)c == 'l' || (char)c == 'h')
		return (1);
	else
		return (0);
}
